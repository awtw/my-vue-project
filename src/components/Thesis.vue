                <p class="text-gray-900 dark:text-white ">慢性腎臟病 ( chronic kidney disease, CKD ) 影響台灣 11.9% 的成年人人口，除此之外 CKD 時常伴隨許多的併發症，例如糖尿病，高血壓及心血管疾病等，易導致病情急轉直下，因此為延緩病人進入末期腎臟病的發生率，國家政府投入大筆資金在提供 CKD 病患長期的醫療照護。若能及早診斷出腎功能的異常，並在腎臟逐漸惡化前加以控制並嚴防進一步惡化，除了能有效給予 CKD 病人就變得更加重要。</p>
                <p class="text-gray-900 dark:text-white ">而近年來的研究指出，腸道的微生物菌相與許多疾病息息相關，有些厭氧菌會在腸道中分寫蛋白質合成後的代謝產物卻對人體產生毒性。在先前研究證實了細菌代謝物的衍生物 “ 親蛋白質尿毒素硫酸吲哚酚 ( indoxyl-sulfate ) ” 高度累積於慢性腎臟病病患的血液中，且對血管細胞產生毒性，加速腎臟功能的惡化。而毒素硫酸吲哚酚 ( indoxyl-sulfate ) 的代謝途徑中，是由色氨酸（Tryptophan）在腸道透過細菌代謝成吲哚（Indole），再由肝臟合成而來。</p>
                <p class="text-gray-900 dark:text-white ">因此評估親蛋白質尿毒素吲哚的量就更顯重要，本研究透過找尋產生親蛋白質尿毒素硫酸吲哚酚的細菌去設計檢測的套組，從核糖核酸的水平開發評估細菌產生吲哚（Indole）能力，去推測末期慢性腎臟病人的病況，未來希望能透過改變慢性腎臟病病人的腸道菌相，大幅減緩慢性腎臟病患的病情發展，並有效改善病人的健康狀態。</p>

             
